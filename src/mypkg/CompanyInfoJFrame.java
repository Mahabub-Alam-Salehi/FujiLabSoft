    private void company_info_tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_company_info_tableMouseClicked
        // TODO add your handling code here:
        
        try
        {
            int row = company_info_table.getSelectedRow();
            String table_click = (company_info_table.getModel().getValueAt(row, 0).toString());
            String sql = "select * from company_info where company_id = '"+table_click+"'";
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            
            if(rs.next())
            {
                String add1 = rs.getString("company_id");
                txt_companyid.setText(add1);
                
                String add2 = rs.getString("company_name");
                txt_companyname.setText(add2);
                
                String add3 = rs.getString("short_name");
                txt_shortname.setText(add3);
                
                String add4 = rs.getString("address");
                txt_address.setText(add4);
                
                String add5 = rs.getString("phone");
                txt_phone.setText(add5);
                
                String add6 = rs.getString("fax");
                txt_fax.setText(add6);
                
                String add7 = rs.getString("email_address");
                txt_email.setText(add7);
                
                String add8 = rs.getString("url");
                txt_url.setText(add8);
                
                
                
                
            }
            
        }catch(Exception e )
        {
            JOptionPane.showMessageDialog(null, e);
            
        }
    }//GEN-LAST:event_company_info_tableMouseClicked

