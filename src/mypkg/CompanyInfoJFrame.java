    private void cmd_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmd_saveActionPerformed
        // TODO add your handling code here:
        
        String c_id = txt_companyid.getText();
        String c_name = txt_companyname.getText();
        String address = txt_address.getText();
        String phone = txt_phone.getText();
        String fax = txt_fax.getText();
        String email = txt_email.getText();
        
        if(c_id.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Company Id can not be Empty");
        }
        else if(c_name.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Company name can not be Empty");
        }
        else if(address.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Company address can not be Empty");
        }
        else if(phone.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Phone Number can not be Empty ");
        }
        else if(fax.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Fax Number can not be Empty");
        }
        else if(email.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Email address can not be Empty");
        }
        
       else
       {
        try
        {
        
        String sql = "insert into company_info(company_id, company_name, short_name, address, phone , fax, email_address, url) values(?,?,?,?,?,?,?,? )";
        pst = conn.prepareStatement(sql);
        
        pst.setString(1,txt_companyid.getText());
        pst.setString(2,txt_companyname.getText());
        pst.setString(3,txt_shortname.getText());
        pst.setString(4,txt_address.getText());
        pst.setString(5,txt_phone.getText());
        pst.setString(6,txt_fax.getText());
        pst.setString(7,txt_email.getText());
        pst.setString(8,txt_url.getText());
        
        pst.execute();
        
            JOptionPane.showMessageDialog(null, "Saved");
            clear();
               
        }catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
        Update_table();
        
        
    }             
        
        
    }//GEN-LAST:event_cmd_saveActionPerformed

