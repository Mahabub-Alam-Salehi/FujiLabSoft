    private void cmd_searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmd_searchActionPerformed
        // TODO add your handling code here:
        String b_code = txt_branch_code.getText();
        String b_name = txt_branch_name.getText();
        String pro_code = txt_product_code.getText();
        String f_date = txt_from_date.getDateFormatString();
        String t_date = txt_to_date.getDateFormatString();
        
         if(b_code.isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Branch code can not Empty");
        }
         if(pro_code.isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Product code can not Empty");
        }
        else if(f_date.isEmpty())
        {
            JOptionPane.showMessageDialog(this, "From date can not Empty");
        }
        else if(t_date.isEmpty())
        {
            JOptionPane.showMessageDialog(this, "To date can not be Empty");
        }
        else
        {
            try{
                
            String sql = "select * from branch_stock where branch_code  = '"+b_code+"' and  product_code = '"+pro_code+"' ORDER BY date >= '"+f_date+"' and date <= '"+t_date+"' ";
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            branch_wise_stock_table.setModel(DbUtils.resultSetToTableModel(rs));
            clear();
            
            }catch(Exception e)
            {
                JOptionPane.showMessageDialog(null, e);
            } 
            
        }
    }//GEN-LAST:event_cmd_searchActionPerformed

